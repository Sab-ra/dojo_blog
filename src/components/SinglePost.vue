<template>
  <div class="post">
    <router-link v-bind:to="{ name: 'postDetails', params: { id:post.id }}">
      <h3>{{ post.title }}</h3>
    </router-link>
    <p>{{ snippet }}</p>
    <span v-for="tag in post.tags" v-bind:key="tag">
      #{{ tag }}
    </span>
  </div>
</template>

<script>
import { computed } from 'vue'
export default {
  props: [ 'post' ],
  setup( props ) {
    const snippet = computed( () => {
      return props.post.body.substring( 0, 100 ) + '....'
    })
    return { snippet }
  }
}
</script>

